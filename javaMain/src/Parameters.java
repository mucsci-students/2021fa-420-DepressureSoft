/**
 * Paramters in the UML Class methods. 
 */

public class Parameters {
    private String pName;

    public Parameters(String pName){
        this.pName = pName;
    }

    public String getPName(){
        return this.pName;
    }

    public void setPName(String newPName){
        this.pName = newPName;
    }

}
